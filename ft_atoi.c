int		ft_atoi(char *str)
{
	int		i;
	int		s;
	int		r;

	r = 0;
	i = 0;
	s = 1;
	if (str[0] == '-')
	{
		i++;
		s = -1;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		r = r * 10 + str[i] - '0';
		i++;
	}
	return (s * r);
}
