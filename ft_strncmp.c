#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while ((UC)s1[i] && (UC)s2[i] && (UC)s1[i] == (UC)s2[i] && i <= n)
		i++;
	return ((UC)s1[i] - (UC)s2[i]);
}
